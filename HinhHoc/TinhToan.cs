using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace HinhHoc
{
    internal class TinhToan
    {
        public int tinhtong(int x, int y)
        {
            return x + y;
        }
        public int tinhtong(int x, int y, int z)
        {
            return x + y + z;
        }
    }
}
